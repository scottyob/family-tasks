/**
 * 1. CONTEXT
 *
 * This section defines the "contexts" that are available in the backend API.
 *
 * These allow you to access things when processing a request, like the database, the session, etc.
 */
import { type CreateNextContextOptions } from "@trpc/server/adapters/next";

import { prisma } from "~/server/db";

type CreateContextOptions = Record<string, never>;

/**
 * This helper generates the "internals" for a tRPC context. If you need to use it, you can export
 * it from here.
 *
 * Examples of things you may need it for:
 * - testing, so we don't have to mock Next.js' req/res
 * - tRPC's `createSSGHelpers`, where we don't have req/res
 *
 * @see https://create.t3.gg/en/usage/trpc#-servertrpccontextts
 */
const createInnerTRPCContext = (_opts: CreateContextOptions) => {
  return {
    prisma,
  };
};


function generateRandomName() {
  const adjectives = ["happy", "sad", "angry", "playful", "sleepy", "crazy", "funny", "shy", "brave", "witty"];
  const nouns = ["panda", "lion", "tiger", "elephant", "koala", "monkey", "kangaroo", "giraffe", "zebra", "hippo"];
  const adjective = adjectives[Math.floor(Math.random() * adjectives.length)];
  const noun = nouns[Math.floor(Math.random() * nouns.length)];
  return `${adjective} ${noun}`;
}

/**
 * Initialises a user
 */
async function initUser(username: string) {
  // Find the user from the dabase 
  let user = await prisma.user.findUnique({
    where: {
      id: username
    }
  });
  if(user == null) {
    // Create a user, and user specific group
    user = await prisma.user.create({
      data: {
        id: username,
        name: generateRandomName(),
        gold: 10
      }
    });

  }
  return user;

}

/**
 * This is the actual context you will use in your router. It will be used to process every request
 * that goes through your tRPC endpoint.
 *
 * @see https://trpc.io/docs/context
 */
export const createTRPCContext = async (opts: CreateNextContextOptions) => {
  const userId = opts.req.headers['x-forwarded-user'] as string ?? 'guest';
  const user = await initUser(userId);

  return {
    user,
    ...createInnerTRPCContext({})
  }
};

/**
 * 2. INITIALIZATION
 *
 * This is where the tRPC API is initialized, connecting the context and transformer.
 */
import { initTRPC } from "@trpc/server";
import superjson from "superjson";

const t = initTRPC.context<typeof createTRPCContext>().create({
  transformer: superjson,
  errorFormatter({ shape }) {
    return shape;
  },
});

